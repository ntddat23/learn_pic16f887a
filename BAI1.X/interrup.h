    unsigned int read_dataPortB;

    void __interrupt() isr(void) {
        if (RBIE == 1 && RBIF == 1) {

            read_dataPortB = PORTB & 0b00000110;
            if (read_dataPortB == 0x02) // Check for RB2
            {
                PORTD = 0xFF;
                PORTC = 0xFF;
                i=0;
                dem=0;
            }

            else if (read_dataPortB == 0x04) // Check for RB1
                RE2 = 1;

            RBIF = 0;
        }
    }

    void Initialize_interrupt() {
        RBIF = 0;
        RBIE = 1;
        GIE = 1;
        IOCB = 0x07;

    }
